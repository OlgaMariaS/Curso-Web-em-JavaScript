/*01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
multiplicação e divisão desses valores*/

function soma (valor1, valor2) {
    console.log(`A soma é: ${valor1} + ${valor2} = ${valor1 + valor2}`);
}
soma(10,10);
function multiplicacao (valor1, valor2) {
    console.log(`A multiplicação é: ${valor1} * ${valor2} = ${valor1 * valor2}`);
}
multiplicacao(10,10);
function subtracao (valor1, valor2) {
    console.log(`A subtração é: ${valor1} - ${valor2} = ${valor1 - valor2}`);
}
subtracao(10,10);
function divisao (valor1, valor2) {
    console.log(`A divisão é: ${valor1} / ${valor2} = ${valor1 / valor2}`);
}
divisao(10,10);